void redraw() {
    SDL_Texture *draw_texture = NULL;
    if (video_mode_valid) {
        check_textures();
        SDL_UpdateTexture(texture_raw, NULL, pico_access_surface->pixels, pico_access_surface->pitch);
        if (renderer_targettexture_supported && PICO_SCANVIDEO_SCALING_BLUR) {
#if PICO_SCANVIDEO_SCALING_NEAREST
            SDL_SetHint(SDL_HINT_RENDER_SCALE_QUALITY, "0");
#else
            SDL_SetHint(SDL_HINT_RENDER_SCALE_QUALITY, "2");
#endif
            SDL_SetRenderTarget(renderer, texture_blurred);
            SDL_SetHint(SDL_HINT_RENDER_SCALE_QUALITY, "0");
            SDL_RenderClear(renderer);
            SDL_RenderCopy(renderer, texture_raw, NULL, NULL);
            SDL_SetRenderTarget(renderer, NULL);
            draw_texture = texture_blurred;
        } else {
            draw_texture = texture_raw;
        }
    }
    SDL_RenderClear(renderer);
    if (draw_texture) SDL_RenderCopy(renderer, draw_texture, NULL, NULL);
    SDL_RenderPresent(renderer);
}

void toggle_fullscreen() {
    uint32_t flags = SDL_GetWindowFlags(window);
    if (flags & SDL_WINDOW_FULLSCREEN_DESKTOP) {
        SDL_SetWindowFullscreen(window, 0);
        SDL_ShowCursor(SDL_ENABLE);
    } else {
        SDL_SetWindowFullscreen(window, SDL_WINDOW_FULLSCREEN_DESKTOP);
        SDL_ShowCursor(SDL_DISABLE);
    }
}

void process_events() {
    SDL_Event event;
    while (SDL_WaitEvent(&event)) {
        switch (event.type) {
            case SDL_KEYDOWN: {
                int modifier = event.key.keysym.mod;
                int scancode = event.key.keysym.scancode;

                if (scancode == SDL_SCANCODE_ESCAPE) {
                    uint32_t flags = SDL_GetWindowFlags(window);
                    if (flags & SDL_WINDOW_FULLSCREEN_DESKTOP) {
                        toggle_fullscreen();
                    }
                }
                if ((modifier & KMOD_ALT) &&
                    scancode == SDL_SCANCODE_RETURN) {
                    if (key_states[scancode] == 0) {
                        toggle_fullscreen();
                        key_states[scancode] = 1;
                    }
                } else {
                    key_states[scancode] = 1;
                    if (platform_key_down)
                        platform_key_down(event.key.keysym.scancode, event.key.keysym.sym, event.key.keysym.mod);
                }
                break;
            }
            case SDL_KEYUP:
                key_states[event.key.keysym.scancode] = 0;
                if (platform_key_up)
                    platform_key_up(event.key.keysym.scancode, event.key.keysym.sym, event.key.keysym.mod);
                break;
            case SDL_WINDOWEVENT:
                switch (event.window.event) {
                    case SDL_WINDOWEVENT_SIZE_CHANGED:
                        window_resized();
                    case SDL_WINDOWEVENT_EXPOSED:
                        redraw();
                        break;
                }
                break;
            case SDL_USEREVENT:
                if (event.user.code == DO_UPDATE_SCREEN) {
#ifdef COALESCE_SCREEN_UPDATES
                    mutex_enter_blocking(&update_screen_mutex);
                    update_screen_pending = false;
                    mutex_exit(&update_screen_mutex);
#endif
                    redraw();
                }
                break;
            case SDL_MOUSEBUTTONDOWN:
                switch (event.button.button) {
                    case SDL_BUTTON_LEFT:
                        mouse_down = true;
                        break;
                    default:
                        break;
                }
                if (platform_mouse_button_down) platform_mouse_button_down(event.button.button);
                break;
            case SDL_MOUSEBUTTONUP:
                switch (event.button.button) {
                    case SDL_BUTTON_LEFT:
                        mouse_down = false;
                        break;
                    default:
                        break;
                }
                if (platform_mouse_button_up) platform_mouse_button_up(event.button.button);
                break;
            case SDL_MOUSEMOTION:
                if (platform_mouse_move) platform_mouse_move(event.motion.xrel, event.motion.yrel);
                break;
            case SDL_QUIT:
                if (platform_quit) platform_quit();
                else exit(0);
                break;
        }
    }
}

